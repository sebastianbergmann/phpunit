/**
 * Constraint that asserts that two arrays have the same values, in any order
 *
 * @package    PHPUnit
 * @subpackage Framework_Constraint
 * @author     Sebastian Bergmann <sebastian@phpunit.de>
 * @author     Jesper Skovgaard Nielsen <jesper@runlevel0.dk>
 * @copyright  2001-2014 Sebastian Bergmann <sebastian@phpunit.de>
 * @license    http://www.opensource.org/licenses/BSD-3-Clause  The BSD 3-Clause License
 * @link       http://www.phpunit.de/
 * @since      Class available since Release 4.0.0
 */
class PHPUnit_Framework_Constraint_ArrayValuesMatch extends PHPUnit_Framework_Constraint
{
    /**
     * @param array $matchingArray
     */
    public function __construct($matchingArray)
    {
        $this->matchingArray = array_values($matchingArray);
        sort($this->matchingArray);
    }

    /**
     * Evaluates the constraint for parameter $other. Returns true if the
     * constraint is met, false otherwise.
     *
     * @param  mixed $other Value or object to evaluate.
     * @return bool
     */
    public function matches($other)
    {
        $other = array_values($other);
        sort($other);

        return $other == $this->matchingArray;
    }

    /**
     * Returns a string representation of the constraint.
     *
     * @return string
     */
    public function toString()
    {
        return 'has the same values as '.var_export($this->matchingArray, true);
    }

    /**
     * Returns the description of the failure
     *
     * The beginning of failure messages is "Failed asserting that" in most
     * cases. This method should return the second part of that sentence.
     *
     * @param  mixed  $other Evaluated value or object.
     * @return string
     */
    protected function failureDescription($other)
    {
        return 'an array ' . $this->toString();
    }
}
